var color = "grey";

switch (color) {
    case "blue":
        console.log("Azul");
        break;
    case "green":
        console.log("Verde");
        break;
    case "black":
        console.log("Preto");
        break;
    case "white":
        console.log("Branco");
        break;
    case "pink":
        console.log("Rosa");
        break; 
    default:
        console.log("Não encontrei em meus dados a cor que você procura");  
}